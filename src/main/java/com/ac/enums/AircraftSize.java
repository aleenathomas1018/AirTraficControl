package com.ac.enums;

/**
 * The AircraftSize enum represents the size of an aircraft in the Aircraft Queue System.
 * It can be either LARGE or SMALL.
 */
public enum AircraftSize {
    /**
     * Represents a large-sized aircraft.
     */
    LARGE,

    /**
     * Represents a small-sized aircraft.
     */
    SMALL
}
